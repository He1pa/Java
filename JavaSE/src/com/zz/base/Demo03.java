package com.zz.base;

public class Demo03 {
    public static void main(String[] args) {
        String a = "Hello";
        int num = 10;
        System.out.println(a);
        System.out.println(num);


        //8大基本数据类型
        int num1 = 100;
        byte num2 = 20;
        short num3 = 30;
        long num4 = 30L;//long类型要在数字后加F

        //小数
        float num5 = 50.1F;
        double num6 = 3.1415;


        //字符
        char name1 = 'A';
        String name2 = "xyz"; //String不是基本数据类型


        //布尔值
        boolean flag = true;
        boolean flag2 = false;

    }
}
