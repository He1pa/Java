package com.zz.struct;

public class SwitchDemo02 {
    public static void main(String[] args) {
        String name = "zz";

        switch (name){
            case "zz":
                System.out.println("zz");
                break;
            case  "ZZ":
                System.out.println("ZZ");
                break;
            default:
                System.out.println("ZZZZZZZZZZZZZZZ");

        }
    }

}
